    //It is a mechanism of wrapping the data and code acting data togheter in a single unit

    //It is the ability to hide data and methods from the rest of world
     private int age;
     private String name;
     private int salary;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

